        if not prices:
            return 0

        n = len(prices)
        if k >= n >> 1:
            res = 0
            for i in range(1, len(prices)):
                res += max(0, prices[i] - prices[i - 1])
            return res

        dp = [[[0] * 2 for _ in range(k + 1)] for _ in range(n)]
        for j in range(k + 1):  # åˆå§‹åŒ–é¦–æ—¥
            dp[0][j][1] = -prices[0]
