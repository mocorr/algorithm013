class Solution:
    def maxProfit(self, prices: List[int]) -> int:
        if len(prices) <= 1:
            return 0
        left_min = prices[0]
        earn_max = 0
        for i in range(1, len(prices)):
            left_min = min(left_min, prices[i-1])
            earn_max = max(earn_max, prices[i] - left_min)
        return earn_max
