class Solution:
    def findContentChildren(self, g: List[int], s: List[int]) -> int:
        g.sort()  # kids
        s.sort()  # cookies
        g_p, s_p = 0, 0
        count = 0
        while g_p < len(g) and s_p < len(s):
            if s[s_p] >= g[g_p]:
                count += 1
                g_p += 1
                s_p += 1
            else:
                s_p += 1
        return count

        
