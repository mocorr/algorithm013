class Solution:
    def rob(self, nums: [int]) -> int:
        def _helper(left, right):
            cur, pre = 0, 0
            for num in nums[left: right]:
                cur, pre = max(pre + num, cur), cur
            return cur

        return max(_helper(0, len(nums) - 1), _helper(1, len(nums))) if len(nums) != 1 else nums[0]
