class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        row = [{} for _ in range(9)]
        col = [{} for _ in range(9)]
        box = [{} for _ in range(9)]
        for i in range(9):
            for j in range(9):
                if board[i][j] != '.':
                    val = board[i][j]
                    if val in row[i] or val in col[j] or val in box[i // 3 * 3 + j // 3]:
                        return False
                    row[i][val] = 1
                    col[j][val] = 1
                    box[i // 3 * 3 + j // 3][val] = 1
        return True

