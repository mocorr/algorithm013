class Solution:
    def hammingWeightBinCount(self, n: int) -> int:
        return bin(n).count('1')

    def hammingWeightBin(self, n: int) -> int:
        cnt = 0
        for num in bin(n):
            if num == '1':
                cnt += 1
        return cnt

    def hammingWeightMask(self, n: int) -> int:
        cnt = 0
        mask = 1
        for i in range(32):
            if n & mask != 0:
                cnt += 1
            mask <<= 1
        return cnt

